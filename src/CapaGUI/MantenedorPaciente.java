    public void buscarPaciente(){
       if (this.txtRutPaciente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Debe ingresar un Rut para buscar",
                    "Error de Búsqueda",
                    JOptionPane.ERROR_MESSAGE);
            this.txtRutPaciente.requestFocus();
            return;
        }
        int RutPaciente = Integer.parseInt(this.txtRutPaciente.getText());
        NegocioPaciente negP = new NegocioPaciente();
        Datos Paciente = negP.buscarDatos(RutPaciente);
        
        
         
        
            
        
        if (Paciente.getRutPaciente()!= 0 ) {
            this.txtRutPaciente.setText((String.valueOf(Paciente.getRutPaciente())));// este es el entero verificador
            this.txtNombrePaciente.setText(Paciente.getNombrePaciente());
            this.txtEdadPaciente.setText(Paciente.getEdadPaciente());
            this.txtHoraPaciente.setText(Paciente.getHoraPaciente());
            this.cmbSexoPaciente.setSelectedItem(Paciente.getSexoPaciente());
            this.txtFechaControl.setText(Paciente.getFechaAtencion());
            
        
            this.cmbSistemaSalud.setSelectedItem(Paciente.getNombreSistemaSalud());
            this.cmbTipoSalud.setSelectedItem(Paciente.getTipoSistemaSalud());
              txtRutPaciente.setEnabled(false);
        } else {
            JOptionPane.showMessageDialog(this,
                    "Este Rut no está registrado",
                    "Error de Búsqueda",
                    JOptionPane.ERROR_MESSAGE);

        }
        
    
    }
    
   
