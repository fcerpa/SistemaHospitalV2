    private void jbtGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtGuardarActionPerformed
        
        if (this.txtRutPaciente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Ingresar Rut del Paciente",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtRutPaciente.requestFocus();
            return;
        }
        
         if (this.txtNombrePaciente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Ingresar El nombre del Paciente",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtNombrePaciente.requestFocus();
            return;
        }
        if (this.txtFechaControl.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Ingresar la Fecha de Control",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtFechaControl.requestFocus();
            return;
        }
        if (this.txtHoraPaciente.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this,
                    "Ingresar la Hora del Paciente",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtHoraPaciente.requestFocus();
            return;
        }
        if (this.txtEdadPaciente.getText().isEmpty())  {
            JOptionPane.showMessageDialog(this,
                    "Ingresar La Edad del paciente",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.txtEdadPaciente.requestFocus();
            return;
        }
        if (this.cmbSexoPaciente.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Seleccionar el sexo del paciente",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cmbSexoPaciente.requestFocus();
            return;
        }
        if (this.cmbCirugia.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Seleccionar la cirugia del paciente",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cmbCirugia.requestFocus();
            return;
        }
        if (this.cmbAtencion.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Seleccionar una el tipo atencion",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cmbAtencion.requestFocus();
            return;
        }
        if (this.cmbEspecialidad.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Seleccionar un la especialidad deribada",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cmbEspecialidad.requestFocus();
            return;
        }
        if (this.cmbSistemaSalud.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Seleccionar un sistema de salud",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cmbSistemaSalud.requestFocus();
            return;
        }
        
         if (this.cmbTipoSalud.getSelectedIndex() == 0) {
            JOptionPane.showMessageDialog(this,
                    "Debe Seleccionar un tipo sistema de salud",
                    "Error de Ingreso",
                    JOptionPane.ERROR_MESSAGE);
            this.cmbTipoSalud.requestFocus();
            return;
        }
        
        //////////////SQL QUERY////////////////////
    
            
        NegocioPaciente neg = new NegocioPaciente();
        
        //////////////Instancia a la Clase get and setter////////////////////
        Datos Pac=new Datos();
      
        
     
         // ingreso pacientes --con esto obtengo todo lo ingresado en las cajas de textos 
        Pac.setRutPaciente(Integer.parseInt(this.txtRutPaciente.getText()));
        Pac.setNombrePaciente(this.txtNombrePaciente.getText());
        Pac.setEdadPaciente(this.txtEdadPaciente.getText());
        Pac.setSexoPaciente(this.cmbSexoPaciente.getSelectedItem().toString()); 
        Pac.setHoraPaciente(this.txtHoraPaciente.getText());
        Pac.setTipoAtencion(this.cmbAtencion.getSelectedItem().toString());
        Pac.setFechaAtencion(this.txtFechaControl.getText());
        Pac.setTipoSistemaSalud(this.cmbTipoSalud.getSelectedItem().toString());
        Pac.setNombreSistemaSalud(this.cmbSistemaSalud.getSelectedItem().toString());
        Pac.setTipoCirugia(this.cmbCirugia.getSelectedItem().toString());
        Pac.setNombreEspecialidad(this.cmbEspecialidad.getSelectedItem().toString());
        neg.ingresoPaciente(Pac);
      
    
        JOptionPane.showMessageDialog(rootPane, "Se Agrego Satisfactoriamente");
    }//GEN-LAST:event_jbtGuardarActionPerformed

